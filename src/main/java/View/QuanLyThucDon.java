    private void dgv_thucdonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dgv_thucdonMouseClicked
        // TODO add your handling code here:
        SimpleDateFormat inputDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat outputDateFormat = new SimpleDateFormat("dd/MM/yyyy");

        int i = dgv_thucdon.getSelectedRow();
        TableModel model = dgv_thucdon.getModel();

        txtID.setText(model.getValueAt(i, 0).toString());
        txtDrinkName.setText(model.getValueAt(i, 1).toString());
        txtPrice.setText(model.getValueAt(i, 2).toString());

        try {
            Date startDate = inputDateFormat.parse(model.getValueAt(i, 3).toString());
            Date endDate = inputDateFormat.parse(model.getValueAt(i, 4).toString());

            String formattedStartDate = outputDateFormat.format(startDate);
            String formattedEndDate = outputDateFormat.format(endDate);

            txtdateStart.setText(formattedStartDate);
            txtdateEnd.setText(formattedEndDate);
        } catch (ParseException ex) {
            // Xử lý lỗi nếu cần thiết
            ex.printStackTrace();
        }

        txtID.setEnabled(false);
        btnThem.setEnabled(false);
        btnSua.setEnabled(true);
        btnXoa.setEnabled(true);

    }//GEN-LAST:event_dgv_thucdonMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        Connection con;
        Statement stmt;

        con = Connect.openConnection();
        try {
            stmt = con.createStatement();
            String query = "INSERT INTO drinks(name, price, start_date, end_date) VALUES (?, ?, ?, ?)";
            PreparedStatement pstmt = con.prepareStatement(query);

            pstmt.setString(1, txtDrinkName.getText());
            pstmt.setInt(2, Integer.parseInt(txtPrice.getText()));
            pstmt.setString(3, txtdateStart.getText());
            pstmt.setString(4, txtdateEnd.getText());

            int i = pstmt.executeUpdate();
            if (i > 0) {
                hienThiDanhSachDoUong();
                JOptionPane.showMessageDialog(null, "Thêm thành công!!");
                btnLamMoi.doClick();
            } else {
                JOptionPane.showMessageDialog(null, "Thêm thất bại!!");
            }

            pstmt.close();
        } catch (SQLException ex) {
            Logger.getLogger(QuanLyThucDon.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

