    public void hienThiDanhSachDoUong() {
        try{
            String []colTieuDe1 = new String[]{"Id","Drink Name","Price","Date Start","Date End"};
            List<Drinks> dsdv = DAO.DrinksDAO.getInstance().GetListDrink();
            DefaultTableModel model = new DefaultTableModel(colTieuDe1, 0);
            Object[] row;
            for (int i = 0; i < dsdv.size(); i++) {
                row = new Object[5];
                // GÁN GIÁ TRỊ
                row[0] = dsdv.get(i).getId();
                row[1] = dsdv.get(i).getName();
                row[2] = dsdv.get(i).getPrice();
                row[3] = dsdv.get(i).getStartDate();
                row[4] = dsdv.get(i).getEndDate();
                model.addRow(row);
            }
            dgv_thucdon.setModel(model);
        }catch(Exception e){
            e.getStackTrace();
        }
    }
