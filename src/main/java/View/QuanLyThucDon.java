    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        Connection con = null;
    PreparedStatement pstmt = null;

    try {
        con = Connect.openConnection();
        String query = "DELETE FROM drinks WHERE ID=?";
        pstmt = con.prepareStatement(query);

        // Lấy giá trị của ID từ text field txtID 
        String id = txtID.getText();

        if (id == null || id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một đồ uống để xóa");
            return;
        }

        // Gán giá trị cho PreparedStatement
        pstmt.setString(1, id);

        int result = pstmt.executeUpdate();

        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Đã xóa đồ uống có ID: " + id);
            hienThiDanhSachDoUong();
            btnLamMoi.doClick();
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy đồ uống với ID: " + id);
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Lỗi khi xóa đồ uống với ID: " + txtID.getText());
        ex.printStackTrace();
    } finally {
        // Đóng các kết nối và tài nguyên
        try {
            if (pstmt != null) {
                pstmt.close();
            }
            if (con != null) {
                con.close();
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        Connection con = null;
    PreparedStatement pstmt = null;

    try {
        con = Connect.openConnection();
        String query = "UPDATE drinks SET name=?, price=?, start_date=?, end_date=? WHERE ID=?";
        pstmt = con.prepareStatement(query);

        // Kiểm tra giá trị nhập vào
        String id = txtID.getText();
        if (id == null || id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ID đồ uống!");
            return;
        }

        String drinkName = txtDrinkName.getText();
        if (drinkName == null || drinkName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên đồ uống!");
            return;
        }

        Double price = 0.0;
        try {
            price = Double.parseDouble(txtPrice.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Giá tiền không hợp lệ!");
            return;
        }

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date startDate = null;
        try {
            startDate = dateFormat.parse(txtdateStart.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu không hợp lệ. Định dạng: dd/MM/yyyy");
            return;
        }

        Date endDate = null;
        try {
            endDate = dateFormat.parse(txtdateEnd.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Ngày kết thúc không hợp lệ. Định dạng: dd/MM/yyyy");
            return;
        }

        // Gán giá trị cho PreparedStatement
        pstmt.setString(1, drinkName);
        pstmt.setDouble(2, price);
        pstmt.setDate(3, new java.sql.Date(startDate.getTime()));
        pstmt.setDate(4, new java.sql.Date(endDate.getTime()));
        pstmt.setString(5, id);

        int result = pstmt.executeUpdate();
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Cập nhật đồ uống thành công!!");
            hienThiDanhSachDoUong();
            btnLamMoi.doClick();
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật đồ uống thất bại!!");
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Có lỗi xảy ra. Không thể cập nhật đồ uống vào CSDL!!");
    } finally {
        // Đóng các kết nối và tài nguyên
        try {
            if (pstmt != null) {
                pstmt.close();
            }
            if (con != null) {
                con.close();
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_btnSuaActionPerformed

